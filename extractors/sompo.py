from typing import List
import json

def _ensure_output_keys(fields: List[str]) -> List[str]:
    return list(dict.fromkeys(fields + ["保険会社", "プラン", "プラン識別子"]))

def build_sompo_step1_prompt() -> str:
    """ステップ1: Sompo Japanの基本情報の抽出"""
    return (
        "あなたは火災保険見積書の解析システムです。以下の場所から正確にデータを抽出してください。\n"
        "1. 氏名: 1枚目上部の宛名（「様」や「御中」の直前まで）\n"
        "2. 所在地: 1枚目の「保険の対象」周辺にある所在地情報\n"
        "3. 建築年月: 1枚目上部の「＜建築年月＞」の直後の値\n"
        "4. 広さ: 1枚目上部の「＜専（占）有面積＞」の直後の値。空欄の場合は「＜延床面積＞」の直後の値\n"
        "5. 建物構造: 1枚目上部の「＜構造級別＞」の直後の値\n"
        "6. 物件種別: 1枚目上部の「＜用法＞」の直後の値\n"
        "7. 保険会社: PDF内の会社名・商品名から「損保ジャパン」を出力\n\n"
        "必ず以下のJSONのみを出力してください（マークダウン符号の使用禁止）:\n"
        "{\"氏名\": \"\", \"所在地\": \"\", \"建築年月\": \"\", \"広さ\": \"\", \"建物構造\": \"\", \"物件種別\": \"\", \"保険会社\": \"損保ジャパン\"}"
    )

def build_sompo_step2_prompt(fields: List[str], common_info: dict) -> str:
    """ステップ2: Sompo Japanのプラン比較表からの詳細抽出"""
    output_fields = _ensure_output_keys(fields)
    template_obj = {k: "" for k in output_fields}
    template_obj["保険会社"] = "損保ジャパン"
    template_obj["プラン"] = "プラン１など"
    template_obj["プラン識別子"] = "プラン１など"
    template_json_str = json.dumps([template_obj, template_obj, template_obj], ensure_ascii=False, indent=2)

    return (
        f"以下の【基本情報】を全プランに適用し、1枚目の比較表から各プランの詳細を抽出してください。\n\n"
        f"【基本情報】\n{json.dumps(common_info, ensure_ascii=False)}\n\n"
        "【抽出の絶対ルール】\n"
        "0. 保険会社: 全行に必ず「損保ジャパン」と出力してください。\n"
        "0-1. プラン: PDF上の「ご案内プラン」配下の見出しをそのまま出力してください。例: 「プラン１」「プラン２」「プラン３」。\n"
        "0-2. プラン識別子: プランと同じ値を出力してください。\n"
        "1. 保険金額の抽出箇所:\n"
        "   - 建物_基本_保険金額: 1枚目プラン比較表の「保険金額」の列、「建物」⇒「基本」の行\n"
        "   - 建物_地震_保険金額: 1枚目プラン比較表の「保険金額」の列、「建物」⇒「地震」の行\n"
        "   - 家財_基本_保険金額: 1枚目プラン比較表の「保険金額」の列、「家財」⇒「基本」の行\n"
        "   - 家財_地震_保険金額: 1枚目プラン比較表の「保険金額」の列、「家財」⇒「地震」の行\n"
        "2. 保険期間: 1枚目プラン比較表の各プランの列、「保険期間」の行の「〇年間」のみ\n"
        "3. 保険料: 1枚目プラン比較表の各プランの列、「合計保険料」の行\n"
        "4. 補償の判定: 比較表の該当行が『〇』や『あり』なら『〇』（U+3007）を、空欄や該当なしなら必ず『』（空欄）を出力してください。「なし」「×」は禁止です。\n"
        "5. プラン数: 比較表にある複数のプランをすべて作成してください。\n\n"
        "必ず以下のJSON配列形式のみを出力してください（マークダウン符号の使用禁止）:\n"
        f"{template_json_str}"
    )
