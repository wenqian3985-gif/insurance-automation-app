from typing import List
import json

def _ensure_output_keys(fields: List[str]) -> List[str]:
    return list(dict.fromkeys(fields + ["保険会社", "プラン", "プラン識別子"]))

def build_tokio_step1_prompt() -> str:
    return (
        "あなたは東京海上日動の見積書解析システムです。以下の場所から正確にデータを抽出してください。\n"
        "1. 氏名: 1枚目上部の宛名（「様」や「御中」の直前まで）\n"
        "2. 所在地: 2枚目上部の「所在地」の直後の値\n"
        "3. 建築年月: 2枚目上部の「建築年月」の直後の値\n"
        "4. 広さ: 2枚目上部の「面積」の直後の値\n"
        "5. 建物構造: 2枚目上部の「構造」の直後の値\n"
        "6. 物件種別: 2枚目上部の「物件種別」の直後の値\n"
        "7. 保険会社: 「東京海上日動」を出力\n\n"
        "必ず以下のJSONのみを出力してください（マークダウン符号の使用禁止）:\n"
        "{\"氏名\": \"\", \"所在地\": \"\", \"建築年月\": \"\", \"広さ\": \"\", \"建物構造\": \"\", \"物件種別\": \"\", \"保険会社\": \"東京海上日動\"}"
    )

def build_tokio_step2_prompt(fields: List[str], common_info: dict) -> str:
    output_fields = _ensure_output_keys(fields)
    template_obj = {k: "" for k in output_fields}
    template_obj["保険会社"] = "東京海上日動"
    template_obj["プラン"] = "プラン①など"
    template_obj["プラン識別子"] = "プラン①など"
    template_json_str = json.dumps([template_obj, template_obj, template_obj], ensure_ascii=False, indent=2)

    return (
        f"以下の【基本情報】を全プランに適用し、2枚目の比較表から各プランの詳細を抽出してください。\n\n"
        f"【基本情報】\n{json.dumps(common_info, ensure_ascii=False)}\n\n"
        "【重要】補償項目は『〇』を抽出しないでください。必ず各プラン列の『免責金額』側の値を文字列で抽出してください。免責金額欄が空欄または対象行がない場合のみ空欄にしてください。\n\n"
        "【抽出ルール】\n"
        "0. 保険会社: 全行に必ず「東京海上日動」と出力。\n"
        "0-1. プラン: 「プラン①」「プラン②」「プラン③」。プラン識別子も同じ値。\n"
        "1. 保険金額: 建物_基本/地震、家財_基本/地震は2枚目比較表の各プラン列から抽出。\n"
        "2. 保険期間: 2枚目比較表の各プラン列の期間（例：5年間）。\n"
        "3. 保険料: 2枚目比較表の各プラン列の総払込保険料。\n"
        "4. 免責金額として抽出する補償項目:\n"
        "   - 火災、落雷、破裂・爆発: 2枚目比較表の『免責金額』列、同名行\n"
        "   - 風災、雹(ひょう)災、雪災: 2枚目比較表の『免責金額』列、同名行\n"
        "   - 水災: 2枚目比較表の『免責金額』列、『水災』行\n"
        "   - 破損、汚損等: 2枚目比較表の『免責金額』列、『破損等』行\n"
        "   - 地震・噴火・津波: 2枚目比較表の『免責金額』列、『地震・噴火・津波』行\n"
        "   - 盗難・水濡（ぬ）れ等: 2枚目比較表の『免責金額』列、同名行\n"
        "5. 上記補償項目には『〇』『×』『あり』『なし』を出力しない。例: 0、5万円、10万円、空欄。\n"
        "6. プラン数: 3つを必ず作成。\n\n"
        "必ず以下のJSON配列形式のみを出力してください（マークダウン符号の使用禁止）:\n"
        f"{template_json_str}"
    )
